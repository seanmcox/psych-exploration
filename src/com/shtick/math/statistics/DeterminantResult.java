package com.shtick.math.statistics;

/**
 * @author scox
 * @param <V> 
 *
 */
public class DeterminantResult<V> {
	private V value;

	/**
	 * @param value
	 */
	public DeterminantResult(V value) {
		super();
		this.value = value;
	}

	/**
	 * @return the value
	 */
	public V getValue() {
		return value;
	}
}
